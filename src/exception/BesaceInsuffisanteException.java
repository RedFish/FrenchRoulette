package exception;

public class BesaceInsuffisanteException  extends Exception {
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public BesaceInsuffisanteException(){
		super("Pas assez d'argent dans la besace");
	}
	
	public BesaceInsuffisanteException(int b) {
		super("Pas assez d'argent dans la besace (besace="+b+" doit être supérieure ou egale à zéro)");
	}
}
